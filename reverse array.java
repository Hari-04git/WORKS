public class ReverseArray {
    public static void main(String[] args) {
        int[] arr = {5,4,3,2,1};
        for(int i=0;i<arr.length;i++){
         System.out.print(arr[i]);
        }
        System.out.println();
        for(int i=4;i>=0;i--){
         System.out.println(arr[i]);
}
}
}
